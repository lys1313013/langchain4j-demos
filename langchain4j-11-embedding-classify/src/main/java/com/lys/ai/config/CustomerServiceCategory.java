package com.lys.ai.config;

/**
 * 客户服务分类枚举
 * 定义了客户服务中常见的咨询问题类别
 */
public enum CustomerServiceCategory {

    /**
     * 账单与支付问题
     */
    BILLING_AND_PAYMENTS,

    /**
     * 技术支持问题
     */
    TECHNICAL_SUPPORT,

    /**
     * 账户管理问题
     */
    ACCOUNT_MANAGEMENT,

    /**
     * 产品信息咨询
     */
    PRODUCT_INFORMATION,

    /**
     * 订单状态查询
     */
    ORDER_STATUS,

    /**
     * 退换货问题
     */
    RETURNS_AND_EXCHANGES,

    /**
     * 反馈与投诉
     */
    FEEDBACK_AND_COMPLAINTS
}